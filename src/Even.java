public class Even {

    public static void main(String[] args) {
        int a = 11;
        if (a % 2 == 1) {
            System.out.println("Number is odd");
        } else if (a==0) {
            System.out.println("Number is neither even nor odd");
        } else {
            System.out.println("Number is even");
        }

    }
}
